package com.amalitech.constants;

/** Enumeration representing the different types of financial transactions. */
public enum TransactionType {
  /** Represents a deposit of funds into an account. */
  DEPOSIT,
  /** Represents a withdrawal of funds from an account. */
  WITHDRAWAL,
  /** Represents an incoming transfer of funds from another account. */
  TRANSFER_IN,
  /** Represents an outgoing transfer of funds to another account. */
  TRANSFER_OUT
}
